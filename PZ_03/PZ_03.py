# 3 По заданному номеру дня недели вывести его название
number = int(input("введите число от 1 до 7  "))
if number == 1:
    print('понедельник')
if number == 2:
    print('вторник')
if number == 3:
    print('среда')
if number == 4:
    print('четверг')
if number == 5:
    print('пятница')
if number == 6:
    print('субота')
if number == 7:
    print('воскресенье')